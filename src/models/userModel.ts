import { Schema, model } from 'mongoose';
import { User } from '../interfaces';

const UserSchema = new Schema<User>({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  lastname: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
    unique: true,
    lowercase: true,
  },
  username: {
    type: String,
    required: true,
    trim: true,
    unique: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  }
}, { timestamps: true, versionKey: false });

export default model<User>('User', UserSchema);